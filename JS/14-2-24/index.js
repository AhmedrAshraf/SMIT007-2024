var marks = +prompt("Enter your marks");

if (marks >= 90) {
  document.write("You got A+");
} else if (marks >= 80) {
  document.write("You got A");
} else if (marks >= 60) {
  document.write("You got B");
} else if (marks >= 40) {
  document.write("You are Passed");
} else {
  document.write("You are Failed Nikamy!");
}
